# int data type example 

user_age = 22 
print(user_age)


# float data type example

cgpa = 9.6
print(cgpa)


# tuple data type example

week = ("monday","wednesday","sunday")
print(week)


# string data type example

user_name= "Punar Pratap Singh Sidhu"
print(user_name)


# list data type example

weekl = ["monday","wednesday","sunday"]
